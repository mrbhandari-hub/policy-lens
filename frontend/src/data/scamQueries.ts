// Helper function to get a random query
export function getRandomScamQuery(): string {
  const randomIndex = Math.floor(Math.random() * SCAM_INVESTIGATION_QUERIES.length);
  return SCAM_INVESTIGATION_QUERIES[randomIndex];
}

// Get multiple unique random queries
export function getRandomScamQueries(count: number): string[] {
  const shuffled = [...SCAM_INVESTIGATION_QUERIES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

